void main() {
  int numero = 4;
  switch (numero) {
    case 1:
      print('I');
      break;
    case 2:
      print('II');
      break;
    case 3:
      print('III');
      break;
    case 4:
      print('IV');
      break;
    case 5:
      print('V');
      break;

    default:
      print('indisponivel');
  }
}
